-- Pour calculer la moyenne d'age des étudiants, on peut utiliser la requête SQL suivante :

            SELECT AVG(age) AS moyenne_age FROM etudiants;

-- Affiche :

mysql> SELECT AVG(age) AS moyenne_age FROM etudiants;
+-------------+
| moyenne_age |
+-------------+
|     23.8000 |
+-------------+
1 row in set (0,01 sec)